// Custom preset option (user can fully customize)
export const CUSTOM_PRESET_ID = "custom";

export const getPresetById = (id: string): StylePreset | undefined => {
  return STYLE_PRESETS.find((p) => p.id === id);
};

export const getPromptModifiers = (
  stylePresetId?: string,
  customPromptModifiers?: string
): string => {
  if (!stylePresetId || stylePresetId === CUSTOM_PRESET_ID) {
    return customPromptModifiers || "";
  }

  const preset = getPresetById(stylePresetId);
  return preset?.promptModifiers || "";
};
